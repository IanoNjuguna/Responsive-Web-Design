// DOM Manipulation

// getElementById()
const title = document.getElementsById("main-heading");
console.log(title); // test

// getElementsByClassName()
const classItem = document.getElementsByClassName("list-item");
console.log(classItem); // test

// getElementByTagName()
const tagItem = document.getElementsByClassName("list-item");
console.log(tagItem); //test

// querySelector()
const container = document.querySelector("div");
console.log(container); //test

// querySelectorAll()
const container2 = document.querySelectorAll("div");
console.log(container2); //test